## Operadores básicos, relacionais e lógicos em R
## Basic, relational and logical operators

# Operadores básicos / Basic operators
# Soma / Sum
4 + 4

# Subtração / subtraction
4 - 4

# Multiplicação / Multiplication
4 * 4

# Divisão / Division
4 / 4

# Potência / power rating
4 ^ 2
4 ** 2

# Módulo (resto da divisão) / module
14 %% 3


# Operadores ralacionais / relational operators
# Atribuindo valores / assigning values
a = 7
b = 5

# Operadores / Operators
a > 8
a < 8
a <= 8 
a >= 8
a == 8
a != 8

# Operadores lógicos / logical operators
# And operator
(a == 8) & (b == 6)
(a == 7) & (b == 5)
(a == 8) & (b == 5)

# Or operator
(a == 8) | (b > 5)
(a == 8) | (b >= 5)

# Not operator
a > 8
print (!a > 8)

























